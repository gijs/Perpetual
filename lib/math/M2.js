function M2() {}

M2.identity = function() {
	return [
		[1, 0],
		[0, 1]
	]
}

M2.product = function(m1, m2) {
	return [
		[m1[0][0] * m2[0][0] + m1[0][1] * m2[1][0], m1[0][0] * m2[0][1] + m1[0][1] * m2[1][1]],
		[m1[1][0] * m2[0][0] + m1[1][1] * m2[1][0], m1[1][0] * m2[0][1] + m1[1][1] * m2[1][1]]
	]
}